package hr.fer.oprpp1.hw02.prob1;

/**
 * Token types that can be used
 * {@link #EOF}
 * {@link #WORD}
 * {@link #NUMBER}
 * {@link #SYMBOL}
 */
public enum TokenType {
    /**
     * EOF type, stands for End of file
     */
    EOF,
    /**
     * Word type
     */
    WORD,
    /**
     * Number type
     */
    NUMBER,
    /**
     * Symbol type
     */
    SYMBOL
}
